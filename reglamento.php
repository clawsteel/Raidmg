    <H4 >REGLAS QUE PERMITIRÁN UNA MEJOR CONVIVENCIA ENTRE EL GRUPO</H4>
    <p>Nos presentamos, Somos el grupo de Staff de Raid’s Mg y hemos</p>
    <p>creado el grupo con los siguientes propósitos:</p>
    <p>Conseguir el desarrollo de la comunidad de entrenadores de Monte Grande y sus alrededores;</p>
    <p>Generar un ambiente donde los jugadores de todas las edades puedan sentirse cómodos a la hora de estar jugando por la zona</p>
    <p>Promover la ayuda entre los jugadores a la hora de las distintas actividades, ya sean  éstas incursiones, Community Day’s y eventos/dias especiales.</p>
    <p>Hemos establecidos algunas reglas para favorecer la estadía en el grupo y poder lograr nuestras expectativas:</p>
    <h5>Regla°1 (Integrantes)</h5>
    <p>Todo integrante del grupo es libre de realizar individualmente cualquier incursión que se proponga.
(Se recomienda realizar en solitario o en un pequeño grupo las incursiones de nivel 1, 2 ó 3).
Pero toda incursión que se requiera participación de varias personas (Las de nivel 4 ó 5) deberá ser anunciada con su lugar y horario de realización.
</p>
<h5>Regla°2: (Incursiones)</h5>
    <p>Las incursiones que requieran cuatro o más personas serán avisadas con la mayor cantidad de tiempo posible para poder realizar una lista de asistencia. Se recomienda que se avise antes de que el huevo haya hecho la eclosión en dicho gimnasio.</p>
    <p>Una vez localizada la incursión, se cambiará el titulo del grupo y se creará también una lista para que cada persona que vaya, ponga su nombre y así tenerlo en cuenta por si no llega a tiempo.</p>
    <p>Si no se llega a la incursión en el tiempo acordado pero aún asi estás en camino, se deberá avisar con antelación para evitar el uso del pase. Habrán 10 minutos de tolerancia, pasado éste tiempo no se podrá reclamar nada al respecto..</p>
    <p>Se pide puntualidad a la hora de incursionar. También dado que hay muchos grupos de Monte Grande activos, se les pide que si ya están en nuestra lista que por favor la realicen en el tiempo acordado y con las personas anotadas</p>
    <p>Se les pide que repasen la lista a la hora de incursionar, evitando así que algún participante quede afuera.</p>
 <h5>Regla°3: (Chat General)</h5>   
    <p>Se les pide por favor que se respete el chat una vez estén definidos los detalles de la incursión (título, lista y horario), evitando toda charla que no trate sobre el tema en cuestión.</p>
    <p>En momentos donde no se esté organizando alguna incursión, está permitido el enviar cualquier contenido al chat.</p>
    <p>Queremos que en nuestro chat haya respeto hacia cualquier integrante del grupo, cualquier persona que se sienta intimidada o que no le agrada como lo tratan, les pedimos que se comunique con algún administrador para saber su situación y que hacer al respecto.</p>
<h5>Regla°4: (Lista de Entrenadores)</h5>
    <p>La lista de <a href="./miembros.php">ＡＭＩＧＯＳ</a>  de Raid’s Mg es exclusivamente para el uso de los integrantes del grupo. Si no deseas figurar o dejar de estar en ella, contactá con SoyDament para que pueda quitarte de ahí. </p>
<h5>Regla°5 (PvP/Players Vs Players)</h5>
    <p>Cada mes realizaremos uno ó dos torneos de la copa que esté actualmente. 
Dado el puntaje que te quede nuestro ranking, tendrás la posibilidad de participar en el torneo zonal contra distintas localidades.</p>
